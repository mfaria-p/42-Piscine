#include <unistd.h>

void	write_str(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int ac, char **av)
{
	if (ac >= 1)
	{
		write_str(av[0]);
		write(1, "\n", 1);
	}
}
